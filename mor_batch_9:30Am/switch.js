var no = 2;
switch(no)
    {
        case 1:
            console.log("one");
            break;
        case 2:
            console.log("two");
            break;
        case 3:
            console.log("three");
            break;
        default:
            console.log("invalid");
    }

    if(no == 1)
    {
        console.log("this is form if one");
    }
    else if(no == 2)
    {
        console.log("this is form if two");
    }
    else if(no == 3)
    {
        console.log("this is form if three");
    }
    else
    {
        console.log("this is form if invalid");
    }